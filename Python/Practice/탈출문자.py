# 탈출문자

# \n은 줄바꿈
print('백문이 불여일견 \n 백견이 불여일타')

# 저는 '나도코딩' 입니다. 
print('저는 "나도코딩" 입니다.')
print("저는 '나도코딩' 입니다.")

# \\를 사용하면 "", ''를 포함해서 출력이 가능하다
print('저는 \"나도코딩\" 입니다.')
print('저는 \'나도코딩\' 입니다.')

# \\ : 문장 내에서 \
print('cd "/Users/q/Desktop/Python Workspace"')

# \\ 를 두번 입력하면 \가 표시된다.
print('cd "\\Users\\q\\Desktop\\Python Workspace"')

# \r : 커서를 맨 앞으로 이동
# \r 뒤에 작성된 값이 맨 앞으로 이동하게됨
# 그레서 red의 자리에 pine이 들어감
print('Red Apple \rPine')

# \b : 백스페이스('한 글자 삭제')
print('Redd\b Apple')

# \t : 탭
print('Red\tApple')

