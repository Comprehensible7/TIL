# 절대값을 나타내는 abs
print(abs(-5)) # 5

# n의 몇승을 구하는 함수
print(pow(4, 2)) # 4^2 = 4*4 = 16

# 최대값 최소값
print(max(5,12))
print(min(5,12))

# 반올림
print(round(3.14)) # 3
print(round(4.99)) # 5

# math 함수
# math 라이브러리 내부의 모든것을 이용하겠다는 것
from math import *

# 내림
print(floor(4.99)) # 4

# 올림
print(ceil(3.14)) # 4

# 제곱근
print(sqrt(16)) # 4

