
public class Student {

	private String name;
	private double score;

	// 생성자
	public Student(String name, double score) {
		super();
		this.name = name;
		this.score = score;
	}

	// Getter Setter
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getScore() {
		return score;
	}

	public void setScore(double score) {
		this.score = score;
	}

}
